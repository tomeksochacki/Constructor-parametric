package com.example.constructor.parametric;

public class ConstructorParametric {
    private String name;
    private String color;
    private int number;
    private int age;
    private int year;

    public ConstructorParametric(String name, String color, int number, int age, int year){
        this.name = name;
        this.color = color;
        this.number = number;
        this.age = age;
        this.year = year;
    }
}
